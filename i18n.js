let currentLang = localStorage.getItem('lm-lang') || 'en';
function t(key) { return T[currentLang]?.[key] || T.en[key] || key; }

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('lm-lang', lang);
  applyLang();
}

function cycleLang() {
  const langs = ['en','zh','ja','ko'];
  const idx = (langs.indexOf(currentLang) + 1) % langs.length;
  setLang(langs[idx]);
}

function applyLang() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  const lb = document.getElementById('langBtn');
  if (lb) lb.textContent = 'üåê ' + t('langLabel');
  if (typeof onLangChange === 'function') onLangChange();
}

document.addEventListener('DOMContentLoaded', () => applyLang());

